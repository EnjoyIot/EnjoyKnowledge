/// Canonical directory name for the stage (AI task staging area).
pub const STAGE_DIR: &str = ".enjoyknowledge_stage";

/// Default stage-directory specification — embedded at compile-time.
const STAGE_DEFAULTS_MD_DEFAULT: &str = include_str!("../../tests/fixtures/stage-defaults.md");

/// Generate the `.enjoyknowledge/` skeleton and `knowledge-tasks/` under `project_root`.
///
/// The skeleton shape is determined by `profile.directories()` and
/// `profile.seed_files()`, so different contexts (coding, design, research, …)
/// produce different directory trees.
pub fn generate_skeleton(project_root: &Path, profile: &dyn Profile) -> anyhow::Result<()> {
    let ek = project_root.join(EK_DIR);
    std::fs::create_dir_all(&ek)?;

    let today = chrono::Local::now().format("%Y-%m-%d").to_string();

    for dir in profile.directories() {
        std::fs::create_dir_all(ek.join(dir))?;
    }

    // Write seed files (only if they don't already exist)
    for (rel_path, content) in profile.seed_files() {
        let full = ek.join(rel_path);
        if let Some(parent) = full.parent() {
            std::fs::create_dir_all(parent)?;
        }
        if !full.exists() {
            let resolved = content.replace("__TIMESTAMP__", &today);
            std::fs::write(&full, resolved)?;
        }
    }

    // v0.4.3: Write _meta/kinds.md (kind registry seed)
    let meta_dir = ek.join("_meta");
    std::fs::create_dir_all(&meta_dir)?;
    let kinds_md_path = meta_dir.join("kinds.md");
    if !kinds_md_path.exists() {
        std::fs::write(&kinds_md_path, kinds::init_default_kinds())?;
    }

    // knowledge-tasks/ at project root level
    std::fs::create_dir_all(project_root.join("knowledge-tasks"))?;

    // Generate index.md (OKF table of contents)
    let index_path = ek.join("index.md");
    if !index_path.exists() {
        std::fs::write(
            &index_path,
            format!("---\ndescription: Knowledge base index — auto-generated directory of all knowledge files\ntimestamp: {today}\n---\n\n# Knowledge Index\n\n> Auto-generated by enjoyknowledge. Lists all knowledge files.\n"),
        )?;
    }

    Ok(())
}

