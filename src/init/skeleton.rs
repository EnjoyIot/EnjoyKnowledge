/// Sync the knowledge summary block in AGENTS.md after a knowledge change (add, fix, etc.).
///
/// This reads the current state via `KnowledgeSource::list_entries` and replaces
/// the content between `<!-- enjoyknowledge_LS_START -->` and
/// `<!-- enjoyknowledge_LS_END -->` in the project root's AGENTS.md.
pub fn sync_agents_md_summary(
    project_root: &Path,
    source: &dyn crate::knowledge::KnowledgeSource,
) -> anyhow::Result<()> {
    let agents_path = project_root.join("AGENTS.md");
    if !agents_path.exists() {
        return Ok(());
    }

    let entries = source.list_entries(None, false)?;
    let summary = format_ls_summary(&entries);

    let content = std::fs::read_to_string(&agents_path)?;
    let start_marker = "<!-- enjoyknowledge_LS_START -->";
    let end_marker = "<!-- enjoyknowledge_LS_END -->";

    let Some(start_pos) = content.find(start_marker) else { return Ok(()) };
    let Some(end_pos) = content.find(end_marker) else { return Ok(()) };

    let new_content = format!(
        "{}{start_marker}\n{summary}\n{end_marker}{}",
        &content[..start_pos],
        &content[end_pos + end_marker.len()..]
    );

    std::fs::write(&agents_path, new_content)?;
    Ok(())
}

/// Render knowledge entries as a plain-text summary block for AGENTS.md.
fn format_ls_summary(entries: &[crate::knowledge::KnowledgeEntry]) -> String {
    let mut out = String::new();
    for entry in entries {
        if entry.is_dir {
            let _ = writeln!(out, "{}/", entry.path);
        } else if let Some(desc) = &entry.description {
            let _ = writeln!(out, "  {} → {}", entry.path, desc);
        } else {
            let _ = writeln!(out, "  {}", entry.path);
        }
    }
    out.trim_end().to_string()
}

