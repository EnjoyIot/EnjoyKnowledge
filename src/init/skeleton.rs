/// Generate the `.enjoyknowledge_stage/` directory tree and 8 template files.
pub fn generate_stage_skeleton(project_root: &Path) -> anyhow::Result<()> {
    let stage = project_root.join(STAGE_DIR);

    // Top-level directories
    std::fs::create_dir_all(stage.join("tasks"))?;
    std::fs::create_dir_all(stage.join("drafts"))?;
    std::fs::create_dir_all(stage.join(".archive").join("tasks"))?;

    // 8 template files under tasks/_template/
    let template_dir = stage.join("tasks").join("_template");
    std::fs::create_dir_all(&template_dir)?;

    let today = chrono::Local::now().format("%Y-%m-%d").to_string();

    let templates: &[(&str, &str)] = &[
        ("summary.md", STAGE_TEMPLATE_SUMMARY),
        ("requirements.md", STAGE_TEMPLATE_REQUIREMENTS),
        ("design.md", STAGE_TEMPLATE_DESIGN),
        ("plan.md", STAGE_TEMPLATE_PLAN),
        ("changes.md", STAGE_TEMPLATE_CHANGES),
        ("tests.md", STAGE_TEMPLATE_TESTS),
        ("delivery.md", STAGE_TEMPLATE_DELIVERY),
        ("review.md", STAGE_TEMPLATE_REVIEW),
    ];

    for (rel_path, content) in templates {
        let full = template_dir.join(rel_path);
        if !full.exists() {
            let resolved = content.replace("__TIMESTAMP__", &today);
            std::fs::write(&full, resolved)?;
        }
    }

    Ok(())
}

